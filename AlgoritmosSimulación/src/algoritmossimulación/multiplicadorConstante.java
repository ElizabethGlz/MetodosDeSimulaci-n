    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        n1 = Integer.parseInt(txtX0.getText());
        c1 = Integer.parseInt(txtA.getText());
        veces = Integer.parseInt(txtN.getText());
        String nt = String.valueOf(n1);
        ntm = nt.length();
        numerosgenerados=new double[veces];
        for (int i = 0; i < veces; i++) {
            
            mult = n1 * c1;
            String cua = String.valueOf(mult);
            ctm = cua.length();
            
            if (ctm % 2 == 0) {
                int d1 = (ctm - ntm) / 2;
                int d2 = d1 + ntm;
                
                nn = String.valueOf(mult);
                sub = nn.substring(d1, d2);
                
                n1 = Integer.parseInt(sub);
            } else {
                nc = "0" + cua;
                ctm = nc.length();
                int d1 = (ctm - ntm) / 2;
                int d2 = d1 + ntm;
                nn = nc;
                sub = nn.substring(d1, d2);
                n1 = Integer.parseInt(sub);
            }
            String agregar="0."+sub;
            lista.addElement(agregar);
            numerosgenerados[i]=Double.parseDouble(agregar);
            jList1.setModel(lista);            
        }        
    }//GEN-LAST:event_btnGenerarActionPerformed

