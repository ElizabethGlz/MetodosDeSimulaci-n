    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        // TODO add your handling code here:
        n1 = Integer.parseInt(txtX0.getText());
        String nt1 = String.valueOf(n1);

        n2 = Integer.parseInt(txtX1.getText());
        String nt2 = String.valueOf(n2);

        veces = Integer.parseInt(txtN.getText());
        arreglo = new double[veces];
        String nt = String.valueOf(n1);
        ntm = nt.length();
        if (nt1.length() != nt2.length()) {
            JOptionPane.showMessageDialog(null, "Los numeros deben ser iguales en Longitud");
            txtX0.setText("");
            txtX1.setText("");
        }
        for (int i = 0; i < veces; i++) {

            mult = n1 * n2;
            String nw2 = String.valueOf(n2);
            String cua = String.valueOf(mult);
            ctm = cua.length();

            if (ctm % 2 == 0) {
                int d1 = (ctm - ntm) / 2;
                int d2 = d1 + ntm;
                nn = String.valueOf(mult);
                sub = nn.substring(d1, d2);
                n1 = Integer.parseInt(nw2);
                n2 = Integer.parseInt(sub);
            } else {
                newcua = "0" + cua;
                ctm = newcua.length();

                int d1 = (ctm - ntm) / 2;
                int d2 = d1 + ntm;
                nn = newcua;
                sub = nn.substring(d1, d2);
                n1 = Integer.parseInt(nw2);
                n2 = Integer.parseInt(sub);
            }

            String agregar = "0." + sub;
            arreglo[i]=Double.parseDouble(agregar);
            lista.addElement(agregar);
            jList1.setModel(lista);

        }
       
    }//GEN-LAST:event_btnGenerarActionPerformed

