    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        semilla = Integer.parseInt(txtXo.getText());
        a = Integer.parseInt(txta.getText());
        c = Integer.parseInt(txtc.getText());
        m = Integer.parseInt(txtm.getText());
        veces = Integer.parseInt(txtN.getText());
        numerosgenerados=new double[veces];
        limpiar();
        for (int i = 0; i < veces; i++) {
            x = semilla * a;
            x = x + c;
            x = x % m;
            res = m - 1;
            res = x / res;
            semilla = x;
            lista.addElement(res);
            numerosgenerados[i]=res;
        }

        jList1.setModel(lista);
    }//GEN-LAST:event_jButton1ActionPerformed

